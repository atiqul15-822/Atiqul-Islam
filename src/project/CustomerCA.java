    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/project","root","letMein");
            if(jTextField1.getText().isEmpty()||jTextField2.getText().isEmpty()||jTextField3.getText().isEmpty()||jTextField4.getText().isEmpty()||jTextField5.getText().isEmpty()||jTextField6.getText().isEmpty())
            {
                this.setVisible(false);
                new Invalid_Input(3).setVisible(true);
            }
            else
            {
                String query = "INSERT INTO customer VALUES ("+jTextField1.getText()+", \""+jTextField2.getText()+"\", "+jTextField3.getText()+", \""+jTextField4.getText()+"\" , \""+jTextField5.getText()+"\", "+jTextField6.getText()+")";
                Statement stmnt = con.createStatement();
                stmnt.execute(query);
                this.setVisible(false);
                new Home().setVisible(true);
            }
        }
        catch(Exception e) {
            this.setVisible(false);
            new Wrong_Input(3).setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

